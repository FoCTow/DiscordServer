#----------------------------------------------------------------------------------------------------------
import json
import os
#----------------------------------------------------------------------------------------------------------
from . import attributes as at
#----------------------------------------------------------------------------------------------------------
class Project:
    #------------------------------------------------------------------------------------------------------
    fileDIR = None
    projDIR = None
    dataDIR = None
    #------------------------------------------------------------------------------------------------------
    urlDATA = None
    #------------------------------------------------------------------------------------------------------
    def __init__(self):
        #--------------------------------------------------------------------------------------------------
        self.fileDIR = os.path.dirname(__file__)
        self.projDIR = os.path.dirname(os.path.dirname(self.fileDIR))
        self.dataDIR = "{}\\{}".format(self.projDIR, at.DIR____________DATA)
        #--------------------------------------------------------------------------------------------------
        self.urlDATA = self.readURLS()
    #------------------------------------------------------------------------------------------------------
    def readURLS( self ):
        #--------------------------------------------------------------------------------------------------
        urlFILE = "{}\\{}".format(self.dataDIR, at.FILE___________URLS)
        if not os.path.isfile( urlFILE ): return None
        #--------------------------------------------------------------------------------------------------
        with open(urlFILE) as file: return json.load(file)
    #------------------------------------------------------------------------------------------------------
    def getURL( self, hookNAME ):
        #--------------------------------------------------------------------------------------------------
        if not hookNAME in self.urlDATA: return None
        else: return self.urlDATA[hookNAME]
