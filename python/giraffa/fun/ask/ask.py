async def choose(ctx, options):
    #------------------------------------------------------------------------------------------------------
    author = ctx.author.mention
    giraffa = ctx.bot.user.mention
    #------------------------------------------------------------------------------------------------------
    if not options:
        options = ['...?']
        respons = '....BLEERG??'
    else:
        options = options.split(' ')
        respons = random.choice(options)
    #------------------------------------------------------------------------------------------------------
    await ctx.send( '{}\'s Opshuns:\n{}\n{} : {}'.format(author, '\n'.join(options), giraffa, respons) )
