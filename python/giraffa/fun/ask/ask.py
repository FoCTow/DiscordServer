async def cmd(ctx, question):
    #------------------------------------------------------------------------------------------------------
    responsList = [ 'EEE...?',
                    'EEEEEEEEEEEEEEEEEEEEE?',
                    'EEEEEE? <3',
                    'BLEEGG!',
                    'BLEERRB!',
                    'EE!',
                    'E!!!',
                    'EEEE...',
                    'EE EEE!!',
                    'NE EH!',
                    '....' ]
    #------------------------------------------------------------------------------------------------------
    author = ctx.author.mention
    giraffa = ctx.bot.user.mention
    #------------------------------------------------------------------------------------------------------
    if not question:
        question = '...?'
        respons = responsList[0]
    else:
        respons = random.choice(responsList)
    #------------------------------------------------------------------------------------------------------
    await ctx.send( '{} : {}\n{} : {}'.format(author, question, giraffa, respons) )
